    public void totalBiaya(){
        int jumlahBaris = TbPenjualan.getRowCount();
        int totalBiaya = 0;
        int jumlahBarang, hargaBarang;
        for (int i = 0; i < jumlahBaris; i++) {
            jumlahBarang = Integer.parseInt(TbPenjualan.getValueAt(i, 3).toString());
            hargaBarang = Integer.parseInt(TbPenjualan.getValueAt(i, 4).toString());
            totalBiaya = totalBiaya + (jumlahBarang * hargaBarang);
        }
        tTotalBayar.setText(String.valueOf(totalBiaya));
        tTampil.setText("Rp "+ totalBiaya +",00");
    }
    
    private void autonumber(){
        try {
            Connection c = KoneksiRoti.ConfigDB();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM transaksi ORDER BY Nofaktur DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoFaktur = r.getString("NoFaktur").substring(2);
                String RT = "" +(Integer.parseInt(NoFaktur)+1);
                String Nol = "";
                
                if(RT.length()==1)
                {Nol = "000";}
                else if(RT.length()==2)
                {Nol = "00";}
                else if(RT.length()==3)
                {Nol = "0";}
                else if(RT.length()==4)
                {Nol = "";}
                tNoTrans.setText("RT" + Nol + RT);
            } else {
                tNoTrans.setText("RT0001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
