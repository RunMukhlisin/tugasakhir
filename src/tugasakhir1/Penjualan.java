void tampiltransaksi(){
        model = new DefaultTableModel();
        TbPenjualan.setModel(model);
        model.addColumn("ID Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Harga");
        model.addColumn("Jumlah");
        model.addColumn("Total");
        
        try {
            String sql = "SELECT * FROM transsementara";
            java.sql.Connection conn = KoneksiRoti.ConfigDB();
            java.sql.Statement statement = conn.createStatement();
            java.sql.ResultSet resultSet = statement.executeQuery(sql);
            while(resultSet.next()){
            model.addRow(new Object[]{resultSet.getString("id_barang"),resultSet.getString("Nama_barang"),resultSet.getString("Harga"),resultSet.getString("jumlah"),resultSet.getString("subtotal")});
            }
            TbPenjualan.setModel(model);
            totalBiaya();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


